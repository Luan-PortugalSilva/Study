/* 
Operadores de Comparação

> maior que
>= maior que ou igual a

< menor que
<= menor que ou igual a

=== igualdade estrita (valor e tipo)

!== diferente estrito (valor e tipo)
*/
const comp = 10 > 5
console.log(comp)

const comp1 = 10 >= 5
console.log(comp1)

const comp2 = 10 < 5
console.log(comp2)

const comp3 = 10 <= 5
console.log(comp3)

const comp5 = 10 === 5
console.log(comp5)

const comp7 = 10 !== 5
console.log(comp7)